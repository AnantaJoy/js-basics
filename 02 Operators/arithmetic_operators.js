// arithmetic operators in javascript
// +, -, *, /, %, ++, --

let x = 10;
let y = 3;
console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

// increment (++)
console.log(++x);
console.log(x++);
console.log(x);

// decrement (--)
console.log(--y);
console.log(y--);
console.log(y);
// comparison operators
// ==, ===, !=, !==, >, <, >=, <=

let x = 10;
let y = 20;

// Equality Operators
// Loose Equality Operators
console.log(x == y);
// Strict Equality Operators(type + value) should be same
console.log(x === y);
console.log(x !== y);


// Relational Operators
console.log(x > y);
console.log(x < y);
console.log(x >= y);
console.log(x <= y);